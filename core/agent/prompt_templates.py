# Промпт для принятия решения
DECISION_PROMPT = """Ты — системный модуль принятия решений по опционам. Принимай решение СТРОГО на основе предоставленных данных. 
У тебя НЕТ новостей/ивентов/макро-данных и НЕЛЬЗЯ ссылаться на них или "ощущения рынка".

Входные данные (уже агрегированы системой):
1) market_analysis — JSON с полями, сформированными из:
   - ivr_analysis: значения IVR по опционам + порог threshold для каждого
   - greeks_analysis: gamma/vega concentration, skew (по дельтам)
   - anomalies: volume_spikes, delta_imbalance (direction: call/put/balanced)
   - support_resistance: уровни support/resistance
2) options_summary — краткая сводка доски:
   - total_count, call_count, put_count
   - expirations (список), strikes_range(min/max)
   - low_ivr_count: сколько опционов имеют passes=True (ivr ниже порога из ivr_analysis)

Текущий базовый актив: {underlying}
Целевая экспирация (обязательно используй в ответе): {expiration}
Текущая цена: {underlying_price}
Порог IVR для фильтрации: {ivr_threshold}%

Данные анализа рынка (JSON):
{market_analysis}

Сводка опционов (JSON):
{options_summary}

Правила интерпретации (ТОЛЬКО по данным):
- "Низкий IVR" = low_ivr_count составляет существенную долю от total_count (например, >= 30–50%) И/ИЛИ в ivr_analysis у большинства опционов ivr < {ivr_threshold}.
- "Направленность" оценивай по:
  - anomalies.delta_imbalance.direction ('call'/'put'/'balanced')
  - greeks_analysis.skew.skew (знак и модуль)
  - volume_spikes: если всплески преимущественно в одной стороне (если это можно вывести из символов spikes, где в тикере тип C/P)
- "Риск/локальная зона" оценивай по support_resistance:
  - выше ближайшего support и близко к resistance → ограниченный апсайд / осторожно с call
  - ниже ближайшего resistance и близко к support → ограниченный даунсайд / осторожно с put
  (если уровней нет — прямо укажи, что оценка по уровням недоступна)

Выбор стратегии (только эти 4 или null):
1) strangle — если:
   - IVR низкий (см. правило выше)
   - направленность слабая: delta_imbalance.direction == 'balanced' И abs(skew) ниже порога skew_threshold (или явно "не skewed")
   - нет явного одностороннего объёмного сигнала (volume_spikes не указывают на доминирование C или P)
2) straddle — если:
   - IVR низкий
   - направленность слабая (как выше)
   - но при этом есть признаки потенциального движения/перестройки риска по данным, без новостей:
     например, концентрация gamma или vega высокая (is_concentrated=True) ИЛИ есть объёмные всплески в обе стороны/вблизи ATM (если это видно из списка spikes)
3) call — если:
   - есть направленность вверх: delta_imbalance.direction == 'call' И/ИЛИ skew положительный и значимый (is_skewed=True и skew>0)
   - и (желательно) есть volume_spikes, в которых преобладают Call-тикеры (тип 'C' в символе)
   - и цена НЕ находится непосредственно под сильным resistance (если уровни заданы)
4) put — если:
   - есть направленность вниз: delta_imbalance.direction == 'put' И/ИЛИ skew отрицательный и значимый (is_skewed=True и skew<0)
   - и (желательно) есть volume_spikes, в которых преобладают Put-тикеры (тип 'P' в символе)
   - и цена НЕ находится непосредственно над сильным support (если уровни заданы)
5) null — если:
   - данных мало/неполные: total_count маленький, low_ivr_count==0 при пустом/недостаточном ivr_analysis, или market_analysis содержит ошибку/пропуск
   - сигналы противоречат друг другу (например, delta_imbalance == call, а skew сильно отрицательный), и ты не можешь обосновать выбор на данных
   - IVR по данным не низкий (большинство ivr >= {ivr_threshold})

Важно:
- Всегда возвращай ТОЛЬКО валидный JSON без поясняющего текста вокруг.
- reasoning должен ссылаться на конкретные поля из market_analysis/options_summary и их значения/флаги (ivr, passes, is_concentrated, skew, imbalance, spike_count, direction, уровни).

Верни решение в формате JSON:
{{
  "signal_type": "strangle" | "straddle" | "call" | "put" | null,
  "underlying": "{underlying}",
  "expiration": "{expiration}",
  "strike_call": 89000,
  "strike_put": 88000,
  "strike": 89000,
  "reasoning": "обоснование строго по полям данных (ivr/skew/imbalance/spikes/support_resistance/концентрации греков)",
  "confidence": 0.0,
  "risk_level": "low" | "medium" | "high"
}}
"""

