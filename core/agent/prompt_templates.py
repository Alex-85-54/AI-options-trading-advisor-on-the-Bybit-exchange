# Промпт для принятия решения
DECISION_PROMPT = """Ты - эксперт по торговле опционами. На основе анализа рынка прими решение о входе в позицию.

Анализ рынка:
{market_analysis}

Базовый актив: {underlying}
Текущая цена: ${underlying_price}

Сводка опционов:
{options_summary}

Задача:
Оцени возможность входа в позицию и выбери тип стратегии:

1. **Стрэнгл (strangle)** - если:
   - Низкий IVR (< 25%)
   - Нет явного дисбаланса между Call и Put
   - Есть сжатие волатильности
   - Нейтральное настроение рынка

2. **Стрэддл (straddle)** - если:
   - Низкий IVR
   - Ожидается событие (новости, отчеты)
   - IV еще не взлетела
   - Нет явного направления

3. **Направленная позиция Call** - если:
   - Явный дисбаланс в пользу Call (высокая дельта Call)
   - Бычьи настроения
   - Всплеск объема в Call опционах
   - Цена выше уровня поддержки

4. **Направленная позиция Put** - если:
   - Явный дисбаланс в пользу Put (высокая дельта Put)
   - Медвежьи настроения
   - Всплеск объема в Put опционах
   - Цена ниже уровня сопротивления

5. **Не входить (null)** - если:
   - Высокий IVR (> 50%)
   - Недостаточно данных
   - Высокая неопределенность
   - Нет четких сигналов

Верни решение в формате JSON:
{{
    "signal_type": "strangle" | "straddle" | "call" | "put" | null,
    "underlying": "{underlying}",
    "expiration": "4JAN26",  // дата экспирации (если выбрана позиция)
    "strike_call": 89000,    // для strangle/straddle
    "strike_put": 88000,     // для strangle/straddle
    "strike": 89000,         // для направленных (call/put)
    "reasoning": "Детальное обоснование решения на основе анализа данных",
    "confidence": 0.75,      // уверенность 0-1
    "risk_level": "low" | "medium" | "high"
}}

ВАЖНО:
- Если условий для входа нет, верни signal_type: null
- Всегда указывай reasoning с детальным обоснованием
- Уверенность (confidence) должна отражать реальную уверенность в решении
- Учитывай все предоставленные данные анализа
"""

